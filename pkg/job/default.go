// =============================================================================
// Job Default - 任务重试默认配置
// =============================================================================
// 定义任务重试机制的默认配置常量。
//
// 功能特性:
//   - 默认重试间隔时间
//   - 默认重试超时时间
//   - 默认重试次数
//
// 使用场景:
//   - 任务重试配置
//   - 网络请求重试
//   - 数据库操作重试
//   - 外部服务调用重试
//
// 设计思路:
//   - 提供合理的默认值，适用于大多数场景
//   - 可通过选项模式自定义配置
//   - 避免硬编码，便于统一管理
//
// 项目中的应用:
//   - 所有需要重试的任务
//   - RPC 调用重试
//   - 消息发送重试
//
// =============================================================================
package job

import "time"

const (
	// DefaultRetryJetLag 默认重试间隔时间
	// 每次重试之间的等待时间，避免频繁重试
	// 默认值: 1 秒
	DefaultRetryJetLag = time.Second

	// DefaultRetryTimeout 默认重试超时时间
	// 单次任务执行的最大超时时间
	// 默认值: 2 秒
	DefaultRetryTimeout = 2 * time.Second

	// DefaultRetryNums 默认重试次数
	// 任务失败后的最大重试次数
	// 默认值: 5 次
	DefaultRetryNums = 5
)
