// =============================================================================
// XErr - 错误码定义
// =============================================================================
// 定义项目中使用的统一业务错误码。
//
// 错误码规范:
//   - 6 位数字，前 2 位表示模块，后 4 位表示具体错误
//   - 10xxxx: 通用错误（服务器异常、参数错误、数据库错误）
//   - 可根据业务需要扩展更多错误码
//
// 使用场景:
//   - RPC 服务返回业务错误时使用
//   - HTTP API 响应中返回给客户端
//   - 客户端根据错误码展示不同的提示信息
//
// 项目中的应用:
//   - 所有 RPC 服务：通过 xerr.New(code, msg) 返回业务错误
//   - 所有 API 服务：通过 resultx.ErrHandler 解析错误码并返回给客户端
//
// =============================================================================
package xerr

const (
	// SERVER_COMMON_ERROR 服务器通用错误（100001）
	// 用于未知的服务器内部错误，客户端提示"服务器异常，稍后再尝试"
	SERVER_COMMON_ERROR = 100001

	// REQUEST_PARAM_ERROR 请求参数错误（100002）
	// 用于请求参数校验失败，客户端提示"请求参数有误"
	REQUEST_PARAM_ERROR = 100002

	// DB_ERROR 数据库错误（100003）
	// 用于数据库操作失败，客户端提示"数据库繁忙，稍后再尝试"
	DB_ERROR = 100003
)
