// =============================================================================
// XErr - 错误消息映射
// =============================================================================
// 维护错误码与用户友好提示消息的映射关系。
//
// 功能特性:
//   - 错误码到中文提示消息的映射
//   - 未知错误码自动降级为通用错误消息
//   - 集中管理所有错误提示，便于国际化
//
// 使用场景:
//   - RPC 服务返回错误时获取对应的提示消息
//   - HTTP API 响应中返回给客户端的错误提示
//
// 设计思路:
//   - 错误码和错误消息分离，便于维护
//   - 未知错误码返回通用错误消息，避免暴露内部信息
//   - 消息面向用户，使用友好的中文提示
//
// =============================================================================
package xerr

// codeText 错误码与提示消息的映射表
// key: 错误码（见 err_code.go）
// value: 用户友好的中文提示消息
var codeText = map[int]string{
	SERVER_COMMON_ERROR: "服务器异常，稍后再尝试",
	REQUEST_PARAM_ERROR: "请求参数有误",
	DB_ERROR:            "数据库繁忙，稍后再尝试",
}

// ErrMsg 根据错误码获取对应的提示消息
// 如果错误码不存在，返回通用错误消息
//
// 参数:
//   - errcode: 业务错误码
//
// 返回:
//   - string: 对应的中文提示消息
//
// 示例:
//   msg := xerr.ErrMsg(xerr.DB_ERROR)
//   // 输出: "数据库繁忙，稍后再尝试"
//
//   msg := xerr.ErrMsg(999999)  // 未知错误码
//   // 输出: "服务器异常，稍后再尝试"（降级为通用错误）
func ErrMsg(errcode int) string {
	if msg, ok := codeText[errcode]; ok {
		return msg
	}
	// 未知错误码降级为通用服务器错误消息
	return codeText[SERVER_COMMON_ERROR]
}
