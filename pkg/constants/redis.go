// =============================================================================
// Redis Constants - Redis 键名常量定义
// =============================================================================
// 定义项目中使用的所有 Redis 键名常量，统一管理避免硬编码。
//
// 功能特性:
//   - 统一管理 Redis 键名
//   - 避免键名拼写错误
//   - 便于键名的维护和修改
//
// 使用场景:
//   - 用户认证：存储用户 Token
//   - 在线状态：存储在线用户列表
//   - 缓存数据：存储各种业务数据
//
// 设计思路:
//   - 使用常量定义所有 Redis 键名
//   - 键名使用冒号分隔，体现层级关系
//   - 键名语义化，便于理解和维护
//
// 项目中的应用:
//   - User 服务：存储用户 Token
//   - IM 服务：存储在线用户列表
//   - 所有服务：缓存热点数据
//
// 命名规范:
//   - 使用冒号分隔层级：模块:子模块:功能
//   - 使用小写字母和下划线
//   - 语义清晰，见名知意
//
// =============================================================================
package constants

const (
	// REDIS_SYSTEM_ROOT_TOKEN 系统管理员 Token 键名
	// 用于存储系统管理员（root）的认证 Token
	// 键格式: system:root:token
	// 值类型: string (JWT Token)
	// 使用场景: 管理员登录认证、权限验证
	REDIS_SYSTEM_ROOT_TOKEN string = "system:root:token"

	// REDIS_ONLINE_USER 在线用户集合键名
	// 用于存储当前在线的用户 ID 列表
	// 键格式: online:user
	// 值类型: set (用户 ID 集合)
	// 使用场景: 在线状态查询、消息推送、统计在线人数
	REDIS_ONLINE_USER string = "online:user"
)
